def encrypt_data(data):
    # Placeholder logic for encryption
    encrypted_data = data[::-1]  # Reversing the data as a simple encryption method
    return encrypted_data

def decrypt_data(encrypted_data):
    # Placeholder logic for decryption
    decrypted_data = encrypted_data[::-1]  # Reversing the encrypted data to decrypt
    return decrypted_data
